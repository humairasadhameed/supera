let alienColor: string = 'red';

// Check the alien's color using an if-else chain
if (alienColor === 'green') {
    console.log("Congratulations! You just earned 5 points for shooting down the green alien.");
} else if (alienColor === 'yellow') {
    console.log("Nice shot! You earned 10 points for shooting down the black alien.");
} else if (alienColor === 'red') {
    console.log("Wow! You earned 15 points for shooting down the red alien.");
} else {
    console.log("Unknown alien color. Keep playing!");
}
